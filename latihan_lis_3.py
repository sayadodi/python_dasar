# Data bilangan
data_bilangan = [12, 8, 15, 10, 6]

# Mencari nilai terbesar
nilai_terbesar = max(data_bilangan)

# Mencari nilai terkecil
nilai_terkecil = min(data_bilangan)

# Menghitung rata-rata
jumlah_bilangan = len(data_bilangan)
total_bilangan = sum(data_bilangan)
rata_rata = total_bilangan / jumlah_bilangan

# Menampilkan hasil
print("Data Bilangan:")
print(f"Bilangan: {data_bilangan}")
print(f"Nilai Terbesar: {nilai_terbesar}")
print(f"Nilai Terkecil: {nilai_terkecil}")
print(f"Rata-rata: {rata_rata}")
print(f"Jumlah Bilangan: {jumlah_bilangan}")
