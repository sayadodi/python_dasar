# (**) Digunakan untuk perpangkatan
# (%) Digunakan untuk modulus atau sisa bagi
# (//) Digunakan untuk pembagian dengan dibulatkan
# (/) Digunakan untuk pembagian dengan tidak dibulatkan
# (*) Digunakan untuk perkalian
# (+) Digunakan untuk penjumlahan
# (-) Digunakan untuk pengurangan

angka1 = 10
angka2 = 5
nama_awal = 'Dodi '
nama_akhir = 'Dwi Riskianto'

pembagian = angka1 ** angka2
sisabagi = angka1 % angka2
nama = nama_awal + nama_akhir

print(nama)
print(pembagian, sisabagi)
